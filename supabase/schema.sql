ALTER PUBLICATION supabase_realtime ADD TABLE surebet_ops;
ALTER PUBLICATION supabase_realtime ADD TABLE dg_ops;
ALTER PUBLICATION supabase_realtime ADD TABLE user_configs;
ALTER PUBLICATION supabase_realtime ADD TABLE casas;
ALTER PUBLICATION supabase_realtime ADD TABLE trash_items;
